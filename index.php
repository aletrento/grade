<font color="4D94FF">What's this?</font><br>
If you are an English teacher (or learner) and you need to make quick grammar tests, this website can be a useful resource for you.<br>
It allows you to select the type(s) of exercise you need, as well as one or more grammar topics.<br>
If the database contains any entries which match your search, you will get some exercises.<br><br>

<font color="4D94FF">How do I use it?</font><br>
Everything's quite user-friendly. You'll find <a href="select.php">a form</a> with a set of checkboxes.<br><br>

<font color="4D94FF">What kind of exercises can I get?</font><br>
Five types of exercises are available (so far):
<ul>
<li>multiple choice</li>
<li>cloze</li>
<li>(re)write sentences</li>
<li>correct mistakes</li>
<li>ask questions</li>
</ul> 
Please note that the database used to retrieve the exercises is still under development. Some grammar topics are far from being covered...<br><br>

